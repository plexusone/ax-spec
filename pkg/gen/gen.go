// Package gen generates Go code from x-ax-* extensions in OpenAPI specs.
package gen

import (
	"bytes"
	"go/format"
	"os"
	"sort"
	"strings"
	"text/template"

	"gopkg.in/yaml.v3"
)

// Spec represents a parsed OpenAPI specification.
type Spec struct {
	raw map[string]any
}

// LoadSpec loads an OpenAPI spec from a file.
func LoadSpec(path string) (*Spec, error) {
	data, err := os.ReadFile(path)
	if err != nil {
		return nil, err
	}

	var raw map[string]any
	if err := yaml.Unmarshal(data, &raw); err != nil {
		return nil, err
	}

	return &Spec{raw: raw}, nil
}

// AXMetadata contains extracted x-ax-* data from a spec.
type AXMetadata struct {
	ErrorCodes     map[string]string   // code -> description
	Operations     []OperationMeta     // all operations
	Capabilities   map[string][]string // capability -> operations
	RequiredFields map[string][]string // operationId -> required fields
	RetryPolicy    map[string]bool     // operationId -> retryable
	Idempotent     map[string]bool     // operationId -> idempotent
}

// OperationMeta contains metadata for a single operation.
type OperationMeta struct {
	OperationID  string
	Method       string
	Path         string
	Summary      string
	Capabilities []string
	Retryable    bool
	Idempotent   bool
	Sandboxable  bool
}

// ExtractAXMetadata extracts x-ax-* extensions from a spec.
func ExtractAXMetadata(spec *Spec) (*AXMetadata, error) {
	meta := &AXMetadata{
		ErrorCodes:     make(map[string]string),
		Operations:     []OperationMeta{},
		Capabilities:   make(map[string][]string),
		RequiredFields: make(map[string][]string),
		RetryPolicy:    make(map[string]bool),
		Idempotent:     make(map[string]bool),
	}

	paths, ok := spec.raw["paths"].(map[string]any)
	if !ok {
		return meta, nil
	}

	for pathKey, pathValue := range paths {
		pathItem, ok := pathValue.(map[string]any)
		if !ok {
			continue
		}

		for method, opValue := range pathItem {
			if !isHTTPMethod(method) {
				continue
			}

			operation, ok := opValue.(map[string]any)
			if !ok {
				continue
			}

			opMeta := OperationMeta{
				Method: strings.ToUpper(method),
				Path:   pathKey,
			}

			if opID, ok := operation["operationId"].(string); ok {
				opMeta.OperationID = opID
			}

			if summary, ok := operation["summary"].(string); ok {
				opMeta.Summary = summary
			}

			// Extract x-ax-capabilities
			if caps, ok := operation["x-ax-capabilities"].([]any); ok {
				for _, cap := range caps {
					if s, ok := cap.(string); ok {
						opMeta.Capabilities = append(opMeta.Capabilities, s)
						meta.Capabilities[s] = append(meta.Capabilities[s], opMeta.OperationID)
					}
				}
			}

			// Extract x-ax-retryable
			if retryable, ok := operation["x-ax-retryable"].(bool); ok {
				opMeta.Retryable = retryable
				meta.RetryPolicy[opMeta.OperationID] = retryable
			}

			// Extract x-ax-idempotent
			if idempotent, ok := operation["x-ax-idempotent"].(bool); ok {
				opMeta.Idempotent = idempotent
				meta.Idempotent[opMeta.OperationID] = idempotent
			}

			// Extract x-ax-sandboxable
			if sandboxable, ok := operation["x-ax-sandboxable"].(bool); ok {
				opMeta.Sandboxable = sandboxable
			}

			// Extract x-ax-required-fields
			if fields, ok := operation["x-ax-required-fields"].([]any); ok {
				var reqFields []string
				for _, f := range fields {
					if s, ok := f.(string); ok {
						reqFields = append(reqFields, s)
					}
				}
				meta.RequiredFields[opMeta.OperationID] = reqFields
			}

			// Extract error codes from responses
			if responses, ok := operation["responses"].(map[string]any); ok {
				for statusCode, respValue := range responses {
					if !strings.HasPrefix(statusCode, "4") && !strings.HasPrefix(statusCode, "5") {
						continue
					}
					if resp, ok := respValue.(map[string]any); ok {
						if code, ok := resp["x-ax-error-code"].(string); ok {
							desc := ""
							if d, ok := resp["description"].(string); ok {
								desc = d
							}
							meta.ErrorCodes[code] = desc
						}
					}
				}
			}

			meta.Operations = append(meta.Operations, opMeta)
		}
	}

	return meta, nil
}

func isHTTPMethod(method string) bool {
	switch method {
	case "get", "post", "put", "patch", "delete", "head", "options":
		return true
	}
	return false
}

// Generator generates Go code from AX metadata.
type Generator struct {
	pkg  string
	meta *AXMetadata
}

// New creates a new Generator.
func New(pkg string, meta *AXMetadata) *Generator {
	return &Generator{pkg: pkg, meta: meta}
}

// GenerateErrors generates errors.go with error code constants.
func (g *Generator) GenerateErrors() ([]byte, error) {
	const tmpl = `// Code generated by ax-spec. DO NOT EDIT.

package {{.Package}}

// Error codes from x-ax-error-code extensions.
const (
{{- range $code, $desc := .ErrorCodes}}
	// {{$code}}{{if $desc}} - {{$desc}}{{end}}
	Err{{toPascal $code}} = "{{$code}}"
{{- end}}
)

// IsError checks if an error matches the given AX error code.
func IsError(err error, code string) bool {
	if err == nil {
		return false
	}
	// Check if error message contains the code
	// In practice, integrate with your SDK's error type
	return false
}
`
	return g.execute(tmpl, map[string]any{
		"Package":    g.pkg,
		"ErrorCodes": g.meta.ErrorCodes,
	})
}

// GenerateRetry generates retry.go with retry policies.
func (g *Generator) GenerateRetry() ([]byte, error) {
	const tmpl = `// Code generated by ax-spec. DO NOT EDIT.

package {{.Package}}

// RetryPolicy maps operation IDs to their retry safety.
// true = safe to retry, false = not safe (may cause duplicates).
var RetryPolicy = map[string]bool{
{{- range $op, $retryable := .RetryPolicy}}
	"{{$op}}": {{$retryable}},
{{- end}}
}

// IsRetryable returns whether an operation is safe to retry.
func IsRetryable(operationID string) bool {
	retryable, ok := RetryPolicy[operationID]
	if !ok {
		return false // Default to not retryable
	}
	return retryable
}

// IdempotentOperations maps operation IDs to their idempotency.
var IdempotentOperations = map[string]bool{
{{- range $op, $idem := .Idempotent}}
	"{{$op}}": {{$idem}},
{{- end}}
}

// IsIdempotent returns whether an operation is idempotent.
func IsIdempotent(operationID string) bool {
	idem, ok := IdempotentOperations[operationID]
	return ok && idem
}
`
	return g.execute(tmpl, map[string]any{
		"Package":     g.pkg,
		"RetryPolicy": g.meta.RetryPolicy,
		"Idempotent":  g.meta.Idempotent,
	})
}

// GenerateCapabilities generates capabilities.go with capability mappings.
func (g *Generator) GenerateCapabilities() ([]byte, error) {
	const tmpl = `// Code generated by ax-spec. DO NOT EDIT.

package {{.Package}}

// Capability constants for agent discovery.
const (
{{- range $cap := .CapabilityList}}
	Cap{{toPascal $cap}} = "{{$cap}}"
{{- end}}
)

// CapabilityOperations maps capabilities to operations that provide them.
var CapabilityOperations = map[string][]string{
{{- range $cap, $ops := .Capabilities}}
	"{{$cap}}": { {{- range $i, $op := $ops}}{{if $i}}, {{end}}"{{$op}}"{{end -}} },
{{- end}}
}

// OperationsForCapability returns operations that provide the given capability.
func OperationsForCapability(capability string) []string {
	return CapabilityOperations[capability]
}

// OperationCapabilities maps operations to their capabilities.
var OperationCapabilities = map[string][]string{
{{- range .Operations}}
{{- if .Capabilities}}
	"{{.OperationID}}": { {{- range $i, $cap := .Capabilities}}{{if $i}}, {{end}}"{{$cap}}"{{end -}} },
{{- end}}
{{- end}}
}

// CapabilitiesForOperation returns capabilities provided by the given operation.
func CapabilitiesForOperation(operationID string) []string {
	return OperationCapabilities[operationID]
}
`
	// Build sorted capability list
	caps := make([]string, 0, len(g.meta.Capabilities))
	for cap := range g.meta.Capabilities {
		caps = append(caps, cap)
	}
	sort.Strings(caps)

	return g.execute(tmpl, map[string]any{
		"Package":        g.pkg,
		"CapabilityList": caps,
		"Capabilities":   g.meta.Capabilities,
		"Operations":     g.meta.Operations,
	})
}

// GenerateValidation generates validation.go with required field validators.
func (g *Generator) GenerateValidation() ([]byte, error) {
	const tmpl = `// Code generated by ax-spec. DO NOT EDIT.

package {{.Package}}

// RequiredFields maps operation IDs to their required input fields.
var RequiredFields = map[string][]string{
{{- range $op, $fields := .RequiredFields}}
	"{{$op}}": { {{- range $i, $f := $fields}}{{if $i}}, {{end}}"{{$f}}"{{end -}} },
{{- end}}
}

// GetRequiredFields returns the required fields for an operation.
func GetRequiredFields(operationID string) []string {
	return RequiredFields[operationID]
}
`
	return g.execute(tmpl, map[string]any{
		"Package":        g.pkg,
		"RequiredFields": g.meta.RequiredFields,
	})
}

func (g *Generator) execute(tmpl string, data map[string]any) ([]byte, error) {
	funcMap := template.FuncMap{
		"toPascal": toPascalCase,
	}

	t, err := template.New("gen").Funcs(funcMap).Parse(tmpl)
	if err != nil {
		return nil, err
	}

	var buf bytes.Buffer
	if err := t.Execute(&buf, data); err != nil {
		return nil, err
	}

	// Format Go code
	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		// Return unformatted if formatting fails
		return buf.Bytes(), nil
	}

	return formatted, nil
}

func toPascalCase(s string) string {
	parts := strings.Split(s, "_")
	for i, part := range parts {
		if len(part) > 0 {
			parts[i] = strings.ToUpper(part[:1]) + strings.ToLower(part[1:])
		}
	}
	return strings.Join(parts, "")
}
